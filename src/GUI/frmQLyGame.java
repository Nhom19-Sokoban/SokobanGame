    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
         String idLV = txtID.getText().trim();
    String tenLV = txtTenLV.getText().trim();
    String tenFile = txtfile.getText().trim();
    int cot = (int) SpinnerCot.getValue();
    int dong = (int) SpinnerDong.getValue();

    // Kiểm tra ràng buộc đầu vào
    if (idLV.isEmpty() || tenLV.isEmpty() || tenFile.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin màn chơi (ID, Tên, File)");
        return;
    }

    if (cot < 3 || cot > 50 || dong < 3 || dong > 50) {
        JOptionPane.showMessageDialog(this, "Số cột và dòng phải từ 3 đến 50");
        return;
    }

    LevelSokoban level = new LevelSokoban(idLV, tenLV, tenFile, cot, dong);
    LevelSokobanDAO dao = new LevelSokobanDAO();

    if (dao.insertLevel(level)) {
        JOptionPane.showMessageDialog(this, "Thêm màn chơi thành công");
        loadData(); // Cập nhật lại bảng
    } else {
        JOptionPane.showMessageDialog(this, "Thêm thất bại (có thể trùng ID hoặc tên màn)");
    }


    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblManChoi.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một màn chơi để cập nhật");
            return;
        }

        try {
            // Lấy khóa chính (tenLV) từ bảng
            String tenLV = tblManChoi.getValueAt(selectedRow, 1).toString();

            // Lấy dữ liệu từ form, chú ý: dùng spinner nếu bạn dùng spinner
            String tenFile = txtfile.getText().trim();
            int cot = (int) SpinnerCot.getValue();   // nếu dùng JSpinner
            int dong = (int) SpinnerDong.getValue();

            // Kiểm tra giá trị hợp lệ
            if (cot < 3 || cot > 50 || dong < 3 || dong > 50) {
                JOptionPane.showMessageDialog(this, "Số cột và dòng phải từ 3 đến 50");
                return;
            }

            // Tạo đối tượng LevelSokoban - có thể có constructor đủ tham số hoặc dùng setter
            String idLV = tblManChoi.getValueAt(selectedRow, 0).toString();
            LevelSokoban level = new LevelSokoban(idLV, tenLV, tenFile, cot, dong);
            level.setTenLV(tenLV);
            level.setTenFile(tenFile);
            level.setCot(cot);
            level.setDong(dong);

            LevelSokobanDAO dao = new LevelSokobanDAO();
            if (dao.updateLevel(level)) {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                loadData(); // Cập nhật lại bảng
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
            }

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblManChoiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblManChoiMouseClicked
        // TODO add your handling code here:
        int row = tblManChoi.getSelectedRow();
        if (row != -1) {
            String idLV = tblManChoi.getValueAt(row, 0).toString();
            String tenLV = tblManChoi.getValueAt(row, 1).toString();
            String tenfile = tblManChoi.getValueAt(row, 2) != null ? tblManChoi.getValueAt(row, 2).toString() : "";
            int cot = Integer.parseInt(tblManChoi.getValueAt(row, 3).toString());
            int dong = Integer.parseInt(tblManChoi.getValueAt(row, 4).toString());

            txtID.setText(idLV);
            txtTenLV.setText(tenLV);
            txtfile.setText(tenfile);
            SpinnerCot.setValue(cot);
            SpinnerDong.setValue(dong);
        }
    }//GEN-LAST:event_tblManChoiMouseClicked

