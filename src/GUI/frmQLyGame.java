    private void loadData() {
        try {
            LevelSokobanDAO dao = new LevelSokobanDAO();
            List<LevelSokoban> list = dao.readAllLevels();

            DefaultTableModel model = (DefaultTableModel) tblManChoi.getModel();
            model.setRowCount(0); // Xóa dữ liệu cũ

            for (LevelSokoban lv : list) {
                Object[] row = {
                    lv.getIdLV(),
                    lv.getTenLV(),
                    lv.getTenFile(),
                    lv.getCot(),
                    lv.getDong()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu: " + e.getMessage());
        }
    }

